    public final double lng = 32.748524;


    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_maps);
        SupportMapFragment mapFragment = (SupportMapFragment) getSupportFragmentManager()
                .findFragmentById(R.id.map);
        mapFragment.getMapAsync(this);
    }

    @Override
    public void onMapReady(GoogleMap googleMap) {
        mMap = googleMap;
        LatLng curr = new LatLng(lat, lng);

        mMap.moveCamera(CameraUpdateFactory.newLatLng(curr));

        //
        // Database latitude, longtitude retrival
        //
        double lat2 = 39.875000; // get the  latitude/longtitude from database
        double lng2 = 32.738765;

        addMarker(mMap, lat2, lng2);
    }

    private void addMarker(GoogleMap map, double latitude, double longtitude){
        LatLng temp = new LatLng(latitude, longtitude);
        mMap.addMarker(new MarkerOptions().position(temp));
    }
